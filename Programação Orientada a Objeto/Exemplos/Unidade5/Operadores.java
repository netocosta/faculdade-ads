package Unidade5;

public class Operadores {

    public void multiplicar(float num1, float num2) {
        System.out.println("Multiplicação: " + num1 * num2);
    }

    public void multiplicar(double num1, double num2) {
        System.out.println("Multiplicação: " + num1 * num2);
    }

    public void multiplicar(int num1, int num2) {
        System.out.println("Multiplicação: " + num1 * num2);
    }

}
